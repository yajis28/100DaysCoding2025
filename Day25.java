public class Main {
    public static void main(String[] args) {
        
        int num = 10;
        System.out.println("Nilai awal: " + num);
        
        num++;
        System.out.println("Setelah num++ : " + num);

        ++num;
        System.out.println("Setelah ++num : " + num);

        num--;  
        System.out.println("Setelah num-- : " + num);

        --num; 
        System.out.println("Setelah --num : " + num);
    }
  }
